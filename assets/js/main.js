const input = document.getElementById('inputMessage');
const text = document.getElementById('morsecodeText');
const explanation = document.getElementById('explanation');

function decode() {
    text.innerHTML = ""
    let uppercase = input.value.toUpperCase().split("")
    // console.log(uppercase)
    explanation.innerHTML = "<h1>EXPLANATION</h1>"
    // let splittedText = uppercase.split("");
    // console.log(splittedText)

    uppercase.forEach(element => {
        // console.log(element)

        morseAlphabet.forEach(morse => {
            // console.log(morse);
            if (morse.letter == element) {
                // console.log("working");
                // console.log(morse.morseCode)
                text.innerHTML += morse.morseCode + " "
                explanation.innerHTML += `${element}= ${morse.morseCode}<br>`
            }
        })
    })
}